def function_summ():
    number1 = int(input("Введите первое число: "))
    number2 = int(input("Введите второе число: "))
    summ = number1 + number2
    print(f"Сумма равна = {summ}")


def function_diff():
    number1 = int(input("Введите первое число: "))
    number2 = int(input("Введите второе число: "))
    diff = number1 - number2
    print(f"Разность равна = {diff}")
